const STORAGE_KEY = "catman_lang";
const LANG_LABELS = { uk: "UA", en: "EN", pl: "PL", zh: "中文", de: "DE" };

function applyLanguage(lang) {
  const dict = translations[lang] || translations.uk;
  document.documentElement.lang = lang;

  document.querySelectorAll("[data-i18n]").forEach((el) => {
    const key = el.getAttribute("data-i18n");
    if (dict[key] === undefined) return;
    if (el.hasAttribute("data-i18n-attr")) {
      el.setAttribute(el.getAttribute("data-i18n-attr"), dict[key]);
    } else {
      el.textContent = dict[key];
    }
  });

  document.querySelectorAll("[data-i18n-html]").forEach((el) => {
    const key = el.getAttribute("data-i18n-html");
    if (dict[key] !== undefined) el.innerHTML = dict[key];
  });

  const currentLabel = document.getElementById("langCurrent");
  if (currentLabel) currentLabel.textContent = LANG_LABELS[lang] || "UA";

  document.querySelectorAll(".lang-list li").forEach((li) => {
    li.classList.toggle("active", li.getAttribute("data-lang") === lang);
  });

  try { localStorage.setItem(STORAGE_KEY, lang); } catch (e) {}
}

function initI18n() {
  let saved = "uk";
  try { saved = localStorage.getItem(STORAGE_KEY) || "uk"; } catch (e) {}
  if (!translations[saved]) saved = "uk";
  applyLanguage(saved);

  document.querySelectorAll(".lang-list li").forEach((li) => {
    li.addEventListener("click", () => {
      applyLanguage(li.getAttribute("data-lang"));
      document.getElementById("langList").classList.remove("open");
      document.getElementById("langBtn").setAttribute("aria-expanded", "false");
    });
  });
}

document.addEventListener("DOMContentLoaded", initI18n);
